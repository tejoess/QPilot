# TABLE WITH BORDERS
def add_table_with_border(doc, content):
    table = doc.add_table(rows=1, cols=len(content["headers"]))

    for i, h in enumerate(content["headers"]):
        table.rows[0].cells[i].text = h

    for row in content["rows"]:
        cells = table.add_row().cells
        for i, val in enumerate(row):
            cells[i].text = str(val)

    # Add borders
    for row in table.rows:
        for cell in row.cells:
            tc = cell._tc
            tcPr = tc.get_or_add_tcPr()
            borders = OxmlElement('w:tcBorders')

            for edge in ('top', 'left', 'bottom', 'right'):
                elem = OxmlElement(f'w:{edge}')
                elem.set(qn('w:val'), 'single')
                elem.set(qn('w:sz'), '4')
                elem.set(qn('w:color'), '000000')
                borders.append(elem)

            tcPr.append(borders)


def add_text_with_formula_block(doc, text):
    import re
    from docx.shared import Inches

    parts = re.split(r'(\$.*?\$)', text)

    for part in parts:
        if part.startswith("$") and part.endswith("$"):
            formula = part.strip("$")
            img = generate_formula(formula, "temp_formula.png")

            doc.add_picture(img, width=Inches(2))

        else:
            if part.strip():
                p = doc.add_paragraph(part)
                p.paragraph_format.space_after = 0
                p.paragraph_format.space_before = 0

from docx import Document
from docx.shared import Inches


def generate_docx(questions, filename="paper.docx"):
    doc = Document()

    for q in questions:

        # 🔹 Handle text + formula (BLOCK STYLE)
        add_text_with_formula_block(doc, q["question_text"])

        # 🔹 Handle GDT elements
        for g in q.get("gdt", []):

            if g["type"] == "table":
                add_table_with_border(doc, g["content"])

            elif g["type"] == "plot":
                img = generate_plot(g["content"], f"{q['question_id']}_plot.png")
                doc.add_picture(img, width=Inches(3.5))

            elif g["type"] == "graph_ds":
                img = generate_graph_ds(g["content"], f"{q['question_id']}_graph.png")
                doc.add_picture(img, width=Inches(3.5))

        # 🔹 Minimal spacing between questions
        p = doc.add_paragraph()
        p.paragraph_format.space_after = 4
        p.paragraph_format.space_before = 0

    doc.save(filename)
    print("DOCX saved:", filename)

from reportlab.platypus import SimpleDocTemplate, Paragraph, Image, Spacer, Table, TableStyle
