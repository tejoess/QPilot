def parse_text_with_formula_pdf(text, elements):
    import re
    from reportlab.platypus import Paragraph, Image
    from reportlab.lib.units import inch

    style = styles.getSampleStyleSheet()["Normal"]
    parts = re.split(r'(\$.*?\$)', text)

    for part in parts:
        if part.startswith("$") and part.endswith("$"):
            formula = part.strip("$")
            img = generate_formula(formula, "temp_formula.png")

            elements.append(Image(img, width=2*inch, height=0.4*inch))
        else:
            if part.strip():
                elements.append(Paragraph(part, style))


# TABLE WITH BORDERS (PDF)
def create_pdf_table(content):
    data = [content["headers"]] + content["rows"]

    table = Table(data)
    table.setStyle(TableStyle([
        ('GRID', (0,0), (-1,-1), 1, colors.black),
        ('BACKGROUND', (0,0), (-1,0), colors.lightgrey),
        ('ALIGN', (0,0), (-1,-1), 'CENTER')
    ]))

    return table


# PDF GENERATOR
def generate_pdf(questions, filename="paper.pdf"):
    doc = SimpleDocTemplate(filename)
    elements = []

    for q in questions:
        parse_text_with_formula_pdf(q["question_text"], elements)
        elements.append(Spacer(1, 8))

        for g in q.get("gdt", []):

            if g["type"] == "table":
                elements.append(create_pdf_table(g["content"]))

            elif g["type"] == "plot":
                img = generate_plot(g["content"], f"{q['question_id']}_plot.png")
                elements.append(Image(img, width=3.5*inch, height=2.5*inch))

            elif g["type"] == "graph_ds":
                img = generate_graph_ds(g["content"], f"{q['question_id']}_graph.png")
                elements.append(Image(img, width=3.5*inch, height=2.5*inch))

        elements.append(Spacer(1, 4))

    doc.build(elements)
    print("PDF saved:", filename)



