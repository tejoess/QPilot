def build_prompt(topic, bloom, marks):
    return f"""
You are an expert question paper generator.

INPUT:
- Topic: {topic}
- Bloom Level: {bloom}
- Marks: {marks}

RULES:

1. If marks <= 2:
   - simple question
   - no GDT

2. If marks >= 5 AND bloom in [Apply, Analyze]:
   - MUST include GDT if meaningful

3. GDT TYPES:
- table → comparisons
- plot → numeric / complexity
- graph_ds → trees, graphs

4. Output STRICT JSON:

{{
  "question_id": "QX",
  "question_text": "...",
  "marks": {marks},
  "bloom": "{bloom}",
  "gdt": [
    {{
      "type": "table | plot | graph_ds",
      "content": {{...}}
    }}
  ]
}}

5. Content rules:

table:
- headers
- rows

plot:
- x: list
- y: list

graph_ds:
- directed: true/false
- edges: list

NO explanation. ONLY JSON.

Generate now.
"""

import json

def generate_question(topic, bloom, marks):
    prompt = build_prompt(topic, bloom, marks)

    response = client.chat.completions.create(
        model=MODEL,
        messages=[
            {"role": "system", "content": "You generate structured exam questions."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.3
    )

    text = response.choices[0].message.content.strip()

    # Clean formatting
    text = text.replace("```json", "").replace("```", "").strip()

    try:
        return json.loads(text)
    except:
        print("⚠️ JSON parsing failed:\n", text)
        return None

